// ── File generation ─────────────────────────────────────────────────

function hexToBits(val: number): string {
  let s = "";
  for (let bit = 7; bit >= 0; bit--) {
    s += (val >> bit) & 1 ? "X" : ".";
  }
  return s;
}

function generateGlyphFile(g: GlyphDef): string {
  const cpHex = g.cp.toString(16).toUpperCase().padStart(4, "0");
  const lines: string[] = [];
  lines.push(`# ${g.label} (U+${cpHex})`);
  lines.push("");
  for (let r = 0; r < 16; r++) {
    lines.push(hexToBits(g.rows[r]));
  }
  return lines.join("\n") + "\n";
}

function main(): void {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  let count = 0;
  for (const g of glyphs) {
    const cpHex = g.cp.toString(16).toUpperCase().padStart(4, "0");
    const filename = `U+${cpHex}_${g.label}.glyph`;
    const filePath = path.join(OUTPUT_DIR, filename);
    const content = generateGlyphFile(g);
    fs.writeFileSync(filePath, content, "utf-8");
    count++;
  }

  console.log(`Generated ${count} Arabic remaining glyphs in ${OUTPUT_DIR}`);
}

main();
