// Helper: clone an array and set specific bits
function withMod(base: number[], mods: [number, number][]): number[] {
  const r = [...base];
  for (const [row, val] of mods) {
    r[row] = val;
  }
  return r;
}

// Helper: add dot above (row 3-4 area, centered)
function dotAbove(base: number[], dotRow = 3): number[] {
  return withMod(base, [[dotRow, base[dotRow] | 0x10]]);
}

// Helper: add dot below (row 12-13 area)
function dotBelow(base: number[], dotRow = 12): number[] {
  return withMod(base, [[dotRow, base[dotRow] | 0x10]]);
}

// Helper: add two dots above
function twoDotsAbove(base: number[], dotRow = 3): number[] {
  return withMod(base, [[dotRow, base[dotRow] | 0x24]]);
}

// Helper: add two dots below
function twoDotsBelow(base: number[], dotRow = 12): number[] {
  return withMod(base, [[dotRow, base[dotRow] | 0x24]]);
}

// Helper: add three dots above (triangle)
function threeDotsAbove(base: number[], topRow = 2): number[] {
  return withMod(base, [
    [topRow, base[topRow] | 0x10],
    [topRow + 1, base[topRow + 1] | 0x28],
  ]);
}

// Helper: add three dots below (triangle)
function threeDotsBelow(base: number[], topRow = 12): number[] {
  return withMod(base, [
    [topRow, base[topRow] | 0x10],
    [topRow + 1, base[topRow + 1] | 0x28],
  ]);
}

