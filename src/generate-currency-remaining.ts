import * as fs from 'fs';
import * as path from 'path';

interface GlyphDef { cp: number; label: string; rows: number[]; }

function hexToRow(h: number): string {
  return Array.from({length: 8}, (_, i) => (h & (0x80 >> i)) ? 'X' : '.').join('');
}

function writeGlyph(g: GlyphDef, dir: string): boolean {
  const hex = g.cp.toString(16).toUpperCase().padStart(4, '0');
  const filepath = path.join(dir, `U+${hex}_${g.label}.glyph`);
  if (fs.existsSync(filepath)) {
    console.log(`  SKIP ${filepath} (already exists)`);
    return false;
  }
  const header = `# ${g.label} (U+${hex})\n\n`;
  const grid = g.rows.map(hexToRow).join('\n') + '\n';
  fs.writeFileSync(filepath, header + grid);
  console.log(`  CREATED ${filepath}`);
  return true;
}

