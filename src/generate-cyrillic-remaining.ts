/**
 * Generate missing Cyrillic .glyph files in U+0460–U+048F range.
 *
 * Skips any codepoints that already have glyph files in glyphs/cyrillic/.
 * Covers Old Cyrillic letters, combining marks, and extended characters.
 */

import * as fs from 'fs';
import * as path from 'path';

const PROJECT_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  '..',
);
const OUTPUT_DIR = path.join(PROJECT_ROOT, 'glyphs', 'cyrillic');

interface GlyphDef {
  cp: number;
  label: string;
  rows: number[];
}

function hexToRow(h: number): string {
  return Array.from({ length: 8 }, (_, i) => (h & (0x80 >> i)) ? 'X' : '.').join('');
}

function writeGlyph(g: GlyphDef, dir: string): boolean {
  const hex = g.cp.toString(16).toUpperCase().padStart(4, '0');
  const prefix = `U+${hex}_`;
  const existing = fs.readdirSync(dir).find(f => f.startsWith(prefix) && f.endsWith('.glyph'));
  if (existing) return false;
  const filepath = path.join(dir, `U+${hex}_${g.label}.glyph`);
  if (fs.existsSync(filepath)) return false;
  const header = `# ${g.label} (U+${hex})\n\n`;
  const grid = g.rows.map(hexToRow).join('\n') + '\n';
  fs.writeFileSync(filepath, header + grid);
  return true;
}

