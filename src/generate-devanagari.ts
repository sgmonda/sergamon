/**
 * Generate Devanagari .glyph files for Sergamon font.
 *
 * Generates glyphs for the full Devanagari block U+0900–U+097F.
 * Output directory: glyphs/devanagari/
 */

import fs from "node:fs";
import path from "node:path";

const PROJECT_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  "..",
);
const OUTPUT_DIR = path.join(PROJECT_ROOT, "glyphs", "devanagari");

interface GlyphDef {
  cp: number;
  label: string;
  rows: number[]; // 16 hex values, bit7=leftmost pixel
}

function hexToRow(h: number): string {
  return Array.from({ length: 8 }, (_, i) =>
    h & (0x80 >> i) ? "X" : ".",
  ).join("");
}

function writeGlyph(g: GlyphDef, dir: string): void {
  const hex = g.cp.toString(16).toUpperCase().padStart(4, "0");
  const header = `# ${g.label} (U+${hex})\n\n`;
  const grid = g.rows.map(hexToRow).join("\n") + "\n";
  fs.writeFileSync(path.join(dir, `U+${hex}_${g.label}.glyph`), header + grid);
}

