/**
 * Generate remaining Hebrew block (U+0590-U+05FF) .glyph files for Sergamon font.
 *
 * Generates cantillation marks, nikud (vowel points), punctuation,
 * Yiddish ligatures, and other missing characters.
 *
 * Skips any codepoints that already have glyph files.
 */

import * as fs from 'fs';
import * as path from 'path';

const PROJECT_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  '..',
);
const OUTPUT_DIR = path.join(PROJECT_ROOT, 'glyphs', 'symbols');

interface GlyphDef {
  cp: number;
  label: string;
  rows: number[]; // 16 hex values, bit7=leftmost pixel
}

function hexToRow(h: number): string {
  return Array.from({ length: 8 }, (_, i) => (h & (0x80 >> i)) ? 'X' : '.').join('');
}

function writeGlyph(g: GlyphDef, dir: string) {
  const hex = g.cp.toString(16).toUpperCase().padStart(4, '0');
  const header = `# ${g.label} (U+${hex})\n\n`;
  const grid = g.rows.map(hexToRow).join('\n') + '\n';
  const filepath = path.join(dir, `U+${hex}_${g.label}.glyph`);
  if (!fs.existsSync(filepath)) {
    fs.writeFileSync(filepath, header + grid);
    return true;
  }
  return false;
}

// Check for existing glyphs with any label for a given codepoint
function glyphExists(cp: number, dir: string): boolean {
  const hex = cp.toString(16).toUpperCase().padStart(4, '0');
  const prefix = `U+${hex}_`;
  try {
    const files = fs.readdirSync(dir);
    return files.some(f => f.startsWith(prefix) && f.endsWith('.glyph'));
  } catch {
    return false;
  }
}

