function main(): void {
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }

  console.log(
    `Generating ${glyphs.length} missing math operator glyphs in ${OUTPUT_DIR}`,
  );

  let written = 0;
  let skipped = 0;
  for (const g of glyphs) {
    const hex = g.codepoint.toString(16).toUpperCase().padStart(4, "0");
    const filename = `U+${hex}_${g.name}.glyph`;
    const filepath = path.join(OUTPUT_DIR, filename);
    if (fs.existsSync(filepath)) {
      skipped++;
      console.log(`  SKIP ${filename} (already exists)`);
    } else {
      writeGlyph(g);
      written++;
    }
  }
  console.log(`Done: ${written} written, ${skipped} skipped`);
}

main();
