/**
 * Generate the ~60 missing Mathematical Operator .glyph files (U+2200–U+22FF)
 * for the Sergamon font.
 *
 * Each glyph is an 8x16 pixel grid. Baseline is at row 13.
 * Rows are encoded as hex bytes (bit 7 = leftmost pixel).
 */

import fs from "node:fs";
import path from "node:path";

const PROJECT_ROOT = path.resolve(
  path.dirname(new URL(import.meta.url).pathname),
  "..",
);
const OUTPUT_DIR = path.join(PROJECT_ROOT, "glyphs", "math");

const COLS = 8;
const ROWS = 16;

interface GlyphDef {
  codepoint: number;
  name: string;
  label: string;
  rows: number[]; // 16 hex values, bit7=leftmost
}

// Helper: convert 16 hex row values to grid strings
function rowsToGrid(hexRows: number[]): string[] {
  const lines: string[] = [];
  for (let r = 0; r < ROWS; r++) {
    let line = "";
    const val = hexRows[r] || 0;
    for (let c = 0; c < COLS; c++) {
      line += val & (0x80 >> c) ? "X" : ".";
    }
    lines.push(line);
  }
  return lines;
}

function writeGlyph(def: GlyphDef): void {
  const hex = def.codepoint.toString(16).toUpperCase().padStart(4, "0");
  const filename = `U+${hex}_${def.name}.glyph`;
  const filepath = path.join(OUTPUT_DIR, filename);

  if (fs.existsSync(filepath)) {
    console.log(`  SKIP ${filename} (already exists)`);
    return;
  }

  const grid = rowsToGrid(def.rows);
  const content = `# ${def.label} (U+${hex})\n\n${grid.join("\n")}\n`;
  fs.writeFileSync(filepath, content);
  console.log(`  WROTE ${filename}`);
}

