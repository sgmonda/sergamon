function gridToGlyphContent(name: string, codepoint: number, grid: boolean[][]): string {
  const cpHex = codepoint.toString(16).toUpperCase().padStart(4, "0");
  const lines: string[] = [];

  lines.push(`# ${name} (U+${cpHex})`);
  lines.push("");

  for (let r = 0; r < ROWS; r++) {
    let rowStr = "";
    for (let c = 0; c < COLS; c++) {
      rowStr += grid[r][c] ? "X" : ".";
    }
    lines.push(rowStr);
  }

  return lines.join("\n") + "\n";
}

function findExistingCodepoints(): Set<number> {
  const existing = new Set<number>();
  // Scan all subdirectories under glyphs/
  const subdirs = fs.readdirSync(GLYPHS_ROOT, { withFileTypes: true });
  for (const dirent of subdirs) {
    if (!dirent.isDirectory()) continue;
    const dirPath = path.join(GLYPHS_ROOT, dirent.name);
    const files = fs.readdirSync(dirPath);
    for (const file of files) {
      const match = file.match(/^U\+([0-9A-Fa-f]{4,6})_/);
      if (match) {
        const cp = parseInt(match[1], 16);
        if (cp >= 0x2000 && cp <= 0x206F) {
          existing.add(cp);
        }
      }
    }
  }
  return existing;
}

function main(): void {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });

  const existing = findExistingCodepoints();
  console.log(`Found ${existing.size} existing glyphs in U+2000–U+206F range.`);

  // Filter to only missing codepoints
  const toGenerate = glyphDefs.filter((def) => !existing.has(def.codepoint));

  if (toGenerate.length === 0) {
    console.log("All General Punctuation glyphs already exist. Nothing to generate.");
    return;
  }

  let count = 0;
  for (const def of toGenerate) {
    const cpHex = def.codepoint.toString(16).toUpperCase().padStart(4, "0");
    const filename = `U+${cpHex}_${def.name}.glyph`;
    const filePath = path.join(OUTPUT_DIR, filename);
    const content = gridToGlyphContent(def.name, def.codepoint, def.grid);
    fs.writeFileSync(filePath, content, "utf-8");
    count++;
    console.log(`  Created ${filename}`);
  }

  const total = existing.size + count;
  console.log(
    `\nGenerated ${count} new General Punctuation glyphs in ${OUTPUT_DIR}`,
  );
  console.log(`Total General Punctuation glyphs (U+2000–U+206F): ${total}/112`);
}

main();
