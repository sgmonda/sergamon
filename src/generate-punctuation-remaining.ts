const COLS = 8;
const ROWS = 16;

// Helper: convert an array of hex row values (MSB=leftmost) to a grid
function hexToGrid(hexRows: number[]): boolean[][] {
  const grid: boolean[][] = [];
  for (let r = 0; r < ROWS; r++) {
    const row: boolean[] = [];
    const val = r < hexRows.length ? hexRows[r] : 0;
    for (let c = 0; c < COLS; c++) {
      row.push(((val >> (7 - c)) & 1) === 1);
    }
    grid.push(row);
  }
  return grid;
}

// Helper: create an empty (all dots) grid
function emptyGrid(): boolean[][] {
  return hexToGrid([]);
}

